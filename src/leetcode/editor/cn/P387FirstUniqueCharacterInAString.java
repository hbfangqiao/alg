class Solution {
    public int firstUniqChar(String s) {
        char[] chars = s.toCharArray();
        HashMap<Character,Integer> charMap = new HashMap<>();
        for(char ch : chars){
            charMap.put(ch,(charMap.getOrDefault(ch, 0)) + 1);
        }
        for (int i = 0; i <chars.length ; i++) {
            char ch = chars[i];
            if (charMap.get(ch) == 1){
                return i;
            }
        }
        return -1;
    }
}
