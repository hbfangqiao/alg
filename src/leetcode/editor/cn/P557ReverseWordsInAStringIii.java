class Solution {
    public String reverseWords(String s) {
        String[] ss = s.split(" ");
        for (int i = 0; i <ss.length ; i++) {
            ss[i] = reverse(ss[i]);
        }
        return String.join(" ",ss);
    }

    private String reverse(String s){
        char[] chars = s.toCharArray();
        for (int i = 0,j = chars.length - 1; i < j ; i++,j--) {
            char tmp = chars[i];
            chars[i] = chars[j];
            chars[j] = tmp;
        }
        return new String(chars);
    }
}
